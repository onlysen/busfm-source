		<h3>关于巴士</h3>
		<p>
		巴士电台，是落网站长和朋友们一起制作的一个电台风格的小站，音乐源和落网是一样的，换了的，是聆听音乐的方式。
		这是一个非商业的站点，我们不扯到理想不扯到信仰，也不要问我们运营模式赢利模式，支持我们继续开发下去的动力，不过就是我们对音乐的喜爱。</p><p>
		我们热爱音乐，热爱不同的音乐形式，更希望这些多样且美好的音乐能在更多的人心中播下美好的种子，
		我们，至少是我本人，是相信音乐某种程度上可以改变一个人的世界观的。
		</p>
		<p>
		你，和落网，和巴士，都慢慢被改变了么？
		</p>
